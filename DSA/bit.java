public class bit
{
    public static void main(String args[])
    {
        int a=5;
        System.out.println("011");
        System.out.println(a<<2);
        System.out.println(a>>2);
        
        
        System.out.println("/n/n");
        System.out.println(10);
        System.out.println(10>>5);
        System.out.println(10<<5);
    }
}